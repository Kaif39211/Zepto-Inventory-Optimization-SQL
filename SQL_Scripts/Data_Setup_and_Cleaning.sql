--Percentage of out-of-stock products

SELECT
	COUNT(
		CASE
			WHEN OUT_OF_STOCK = 'TRUE' THEN 1
		END
	) * 100.0 / COUNT(*) AS OUT_OF_STOCK_PERCENTAGE
FROM
	PRODUCT_LEVEL_INVENTORY;

                                       -- Discount Insights


--Products with discount > 20%

SELECT
	CATEGORY,
	DISCOUNT_PERCENT
FROM
	PRODUCT_LEVEL_INVENTORY
WHERE
	DISCOUNT_PERCENT > 21
ORDER BY
	DISCOUNT_PERCENT DESC;

--Average discount per category

SELECT
	CATEGORY,
	ROUND(AVG(DISCOUNT_PERCENT), 2) AS CATEGORY_DISCOUNT
FROM
	PRODUCT_LEVEL_INVENTORY
GROUP BY
	CATEGORY;

--Highest  discounted product

