 --Category-level listing

SELECT DISTINCT
	CATEGORY
FROM
	PRODUCT_LEVEL_INVENTORY;
	

--Count products per category

SELECT
	CATEGORY,
	COUNT(*) AS TOTAL_PRODUCTS
FROM
	PRODUCT_LEVEL_INVENTORY
GROUP BY
	CATEGORY
ORDER BY
	TOTAL_PRODUCTS DESC;


									--Price Analysis

--Top 10 highest MRP products	

SELECT
	NAME,
	CATEGORY,
	MRP
FROM
	PRODUCT_LEVEL_INVENTORY
ORDER BY
	MRP DESC
LIMIT
	10;

--Top 10 lowest discounted price products

SELECT
	NAME,
	CATEGORY,
	DISCOUNTED_SELLING_PRICE
FROM
	PRODUCT_LEVEL_INVENTORY
ORDER BY
	DISCOUNTED_SELLING_PRICE ASC
LIMIT
	10;


--Average MRP per category

SELECT
	CATEGORY,
	ROUND(AVG(MRP), 3) AS AVG_MRP_PER_CATEGORY
FROM
	PRODUCT_LEVEL_INVENTORY
GROUP BY
	CATEGORY;


                                --Stock Status

--Count out-of-stock products

SELECT
	CATEGORY,
	product,
	COUNT(*) AS OUT_OF_STOCK_COUNT
FROM
	PRODUCT_LEVEL_INVENTORY
WHERE
	OUT_OF_STOCK = 'TRUE'
GROUP BY
	CATEGORY,
	product;

